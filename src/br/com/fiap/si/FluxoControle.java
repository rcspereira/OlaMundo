package br.com.fiap.si;

public class FluxoControle {

    public static void main(String[] args) {

        int x = 9;

        if(x > 10){
            System.out.println("X é maior que 10");
        } else if (x==10) {
            System.out.println("X é igual a 10");
        } else {
            System.out.println("X é menor que 10");
        }

    }

}
